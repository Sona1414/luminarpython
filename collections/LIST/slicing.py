lst=[10,25,30,35,40,45,50,55,60]
print(lst[0])
print(lst[1])
print(lst[1:4])#slicing only lower limit value is accpected the upper limit is not read

print(lst[2:6])
print(lst[3:5])
print(lst[2:])# 2 to end of the index is accepted
print(lst[:5])#from 0th index to 4th index
print(lst[:])#full list is printed
print(lst[-1])#negative indexing is starting from the end
print(lst[-3])
print(lst[0]+lst[-2])#10+55=65
print(lst[-0])
print(lst[-3:-1])
print(lst[-1:-3])
print(lst[-3:])
print(lst[:])